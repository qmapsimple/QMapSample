#ifndef ICOORTOOLFUNINTERFACE_H
#define ICOORTOOLFUNINTERFACE_H

#define ICOORTOOLFUNINTERFACE_NAME "CoorToolFunControl"
#include "IPlugInterface.h"

//坐标系转换工具接口类,接口名称为CoorToolFunControl
class ICoorToolFunInterface:public IPlugInterface
{
public:
    ////////////////////////////////////////////////////////////////////////////////////////////////
    // 函数名：RelativeRAE
    // 参数: FirstPos-- 第一点大地地理(经纬高)坐标系位置
    //      SecondPos--第二点大地地理(经纬高)坐标系位置
    // 返回值: 第二点相对第一点的球坐标系位置（径向距，方位角，俯仰角）
    // 函数说明：两点相对球坐标位置计算
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual struSphereCoordPos RelativeRAE(struGeogCoordPos FirstPos, struGeogCoordPos SecondPos)=0;


    ////////////////////////////////////////////////////////////////////////////////////////////////
    // 函数名：CalDist
    // 参数: FirstPos-- 第一点大地地理(经纬高)坐标系位置
    //      SecondPos--第二点大地地理(经纬高)坐标系位置
    // 返回值: 两点球面距离（单位：km）
    // 函数说明：两点球面距离计算
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual double CalDist(struGeogCoordPos FirstPos, struGeogCoordPos SecondPos)=0;




    ////////////////////////////////////////////////////////////////////////////////////////////////
    // 函数名：Rad2Deg
    // 参数: dRadian-- 弧度值
    // 返回值: 角度值
    // 函数说明：弧度转换为角度
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual double Rad2Deg(double dRadian)=0;

    ////////////////////////////////////////////////////////////////////////////////////////////////
    // 函数名：Deg2Rad
    // 参数: dDegree-- 角度值
    // 返回值: 弧度值
    // 函数说明：角度转换为弧度
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual double Deg2Rad(double dDegree)=0;

    ////////////////////////////////////////////////////////////////////////////////////////////////
    // 函数名：swap
    // 参数: double &a-参数1
    //      double &b-参数2
    // 返回值: 弧度值
    // 函数说明：置换函数，互换a和b的值
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual void swap(double &a,double &b)=0;

    ////////////////////////////////////////////////////////////////////////////////////////////////
    // 函数名：DegMinSec2Deg
    // 参数: struDegMinSec DegMinSec-度分秒
    // 返回值: double-度数
    // 函数说明：度分秒转为度
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual double DegMinSec2Deg(struDegMinSec DegMinSec)=0;


    ////////////////////////////////////////////////////////////////////////////////////////////////
    // 函数名：Deg2DegMinSec
    // 参数: double-度数
    // 返回值: struDegMinSec DegMinSec-度分秒
    // 函数说明：度转为度分秒
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual struDegMinSec Deg2DegMinSec(double dDeg)=0;


};

#endif // ICOORTOOLFUNINTERFACE_H
